// Функция для получения гороскопа по имени
function getNameHoroscope(name) {
    const firstLetter = name.charAt(0).toUpperCase();
    
    if (nameHoroscopes[firstLetter]) {
        return {
            horoscope: nameHoroscopes[firstLetter].horoscope,
            advice: nameHoroscopes[firstLetter].advice
        };
    } else {
        // Универсальный гороскоп для имен, начинающихся с других букв
        return {
            horoscope: 'Ваше имя обладает особой энергетикой и уникальной вибрацией. Вы - неповторимая личность с собственным путем развития. Звезды благоволят тем, кто не боится быть собой и следует зову сердца. Впереди вас ждут интересные события и встречи, которые помогут раскрыть ваш потенциал.',
            advice: 'Следуйте своей интуиции и не бойтесь изменений - они приведут вас к новым возможностям и гармонии.'
        };
    }
}

// Обработчики событий для формы гороскопа по имени
document.addEventListener('DOMContentLoaded', function() {
    const nameInput = document.getElementById('user-name');
    const getHoroButton = document.getElementById('get-name-horo');
    const nameResult = document.getElementById('name-result');
    const resultName = document.getElementById('result-name');
    const nameHoroText = document.getElementById('name-horo-text');
    const nameAdviceText = document.getElementById('name-advice-text');
    const tryAnotherButton = document.getElementById('try-another');

    // Валидация имени - только буквы
    nameInput.addEventListener('input', function() {
        this.value = this.value.replace(/[^a-zA-Zа-яА-ЯёЁ\s]/g, '');
    });

    // Получение гороскопа
    getHoroButton.addEventListener('click', function() {
        const userName = nameInput.value.trim();
        
        if (!userName) {
            alert('Пожалуйста, введите ваше имя');
            return;
        }

        if (userName.length < 2) {
            alert('Имя должно содержать хотя бы 2 буквы');
            return;
        }

        const horoscope = getNameHoroscope(userName);
        
        resultName.textContent = userName;
        nameHoroText.textContent = horoscope.horoscope;
        nameAdviceText.textContent = horoscope.advice;
        
        nameResult.classList.remove('hidden');
        nameInput.value = ''; // Очищаем поле ввода
    });

    // Попробовать другое имя
    tryAnotherButton.addEventListener('click', function() {
        nameResult.classList.add('hidden');
        nameInput.focus();
    });

    // Enter для отправки формы
    nameInput.addEventListener('keypress', function(e) {
        if (e.key === 'Enter') {
            getHoroButton.click();
        }
    });
});