function getTodayDate() {
    const now = new Date();
    return formatDate(now);
}

function getTomorrowDate() {
    const now = new Date();
    const tomorrow = new Date(now);
    tomorrow.setDate(now.getDate() + 1);
    return formatDate(tomorrow);
}

function formatDate(date) {
    const options = { day: 'numeric', month: 'long', year: 'numeric' };
    return date.toLocaleDateString('ru-RU', options);
}

// –§—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Å—Ç—Ä–∞–Ω–∏—Ü—ã —Ç–µ—Å—Ç–∞
function initTestPage() {
    const startButton = document.getElementById('start-test');
    const prevButton = document.getElementById('prev-question');
    const nextButton = document.getElementById('next-question');
    const restartButton = document.getElementById('restart-test');
    
    let currentQuestion = 0;
    let userAnswers = [];
    
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –Ω–∞—á–∞–ª–∞ —Ç–µ—Å—Ç–∞
    startButton.addEventListener('click', startTest);
    
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –ø–æ —Ç–µ—Å—Ç—É
    prevButton.addEventListener('click', showPreviousQuestion);
    nextButton.addEventListener('click', showNextQuestion);
    
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞ —Ç–µ—Å—Ç–∞
    if (restartButton) {
        restartButton.addEventListener('click', restartTest);
    }
    
    function startTest() {
        document.getElementById('test-start').classList.add('hidden');
        document.getElementById('test-questions').classList.remove('hidden');
        showQuestion(0);
    }
    
    function showQuestion(index) {
        const question = personalityTest.questions[index];
        const questionText = document.getElementById('question-text');
        const answersContainer = document.getElementById('answers-container');
        const progressFill = document.getElementById('progress-fill');
        const progressText = document.getElementById('progress-text');
        
        // –û–±–Ω–æ–≤–ª—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å
        const progress = ((index + 1) / personalityTest.questions.length) * 100;
        progressFill.style.width = `${progress}%`;
        progressText.textContent = `–í–æ–ø—Ä–æ—Å ${index + 1} –∏–∑ ${personalityTest.questions.length}`;
        
        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –≤–æ–ø—Ä–æ—Å
        questionText.textContent = question.text;
        
        // –û—á–∏—â–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä —Å –æ—Ç–≤–µ—Ç–∞–º–∏
        answersContainer.innerHTML = '';
        
        // –î–æ–±–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç—ã
        question.answers.forEach((answer, i) => {
            const answerBtn = document.createElement('button');
            answerBtn.className = 'answer-btn';
            if (userAnswers[index] === i) {
                answerBtn.classList.add('selected');
            }
            answerBtn.textContent = answer.text;
            answerBtn.addEventListener('click', () => selectAnswer(index, i));
            answersContainer.appendChild(answerBtn);
        });
        
        // –û–±–Ω–æ–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
        prevButton.classList.toggle('hidden', index === 0);
        nextButton.classList.toggle('hidden', index === personalityTest.questions.length - 1);
        
        if (index === personalityTest.questions.length - 1 && userAnswers[index] !== undefined) {
            nextButton.textContent = '–ó–∞–≤–µ—Ä—à–∏—Ç—å —Ç–µ—Å—Ç';
        } else {
            nextButton.textContent = '–î–∞–ª–µ–µ';
        }
        
        currentQuestion = index;
    }
    
    function selectAnswer(questionIndex, answerIndex) {
        userAnswers[questionIndex] = answerIndex;
        
        // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞
        const answerButtons = document.querySelectorAll('.answer-btn');
        answerButtons.forEach((btn, i) => {
            if (i === answerIndex) {
                btn.classList.add('selected');
            } else {
                btn.classList.remove('selected');
            }
        });
        
        // –ê–∫—Ç–∏–≤–∏—Ä—É–µ–º –∫–Ω–æ–ø–∫—É "–î–∞–ª–µ–µ", –µ—Å–ª–∏ –æ–Ω–∞ –±—ã–ª–∞ —Å–∫—Ä—ã—Ç–∞
        if (questionIndex === currentQuestion) {
            nextButton.classList.remove('hidden');
        }
    }
    
    function showPreviousQuestion() {
        if (currentQuestion > 0) {
            showQuestion(currentQuestion - 1);
        }
    }
    
    function showNextQuestion() {
        if (userAnswers[currentQuestion] === undefined) {
            alert('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ –æ—Ç–≤–µ—Ç –Ω–∞ —Ç–µ–∫—É—â–∏–π –≤–æ–ø—Ä–æ—Å');
            return;
        }
        
        if (currentQuestion < personalityTest.questions.length - 1) {
            showQuestion(currentQuestion + 1);
        } else {
            // –ó–∞–≤–µ—Ä—à–∞–µ–º —Ç–µ—Å—Ç –∏ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
            finishTest();
        }
    }
    
    function finishTest() {
        // –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ —ç–ª–µ–º–µ–Ω—Ç–∞–º
        const elementCount = {
            fire: 0,
            water: 0,
            air: 0,
            earth: 0
        };
        
        userAnswers.forEach((answerIndex, questionIndex) => {
            const question = personalityTest.questions[questionIndex];
            const element = question.answers[answerIndex].element;
            elementCount[element]++;
        });
        
        // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –¥–æ–º–∏–Ω–∏—Ä—É—é—â–∏–π —ç–ª–µ–º–µ–Ω—Ç
        let dominantElement = 'fire';
        let maxCount = elementCount.fire;
        
        for (const element in elementCount) {
            if (elementCount[element] > maxCount) {
                maxCount = elementCount[element];
                dominantElement = element;
            }
        }
        
        // –ü–æ–ª—É—á–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        const result = personalityTest.results[dominantElement];
        const randomSignIndex = Math.floor(Math.random() * result.signs.length);
        const resultSign = result.signs[randomSignIndex];
        
        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
        document.getElementById('test-questions').classList.add('hidden');
        document.getElementById('test-result').classList.remove('hidden');
        
        const resultSignImg = document.getElementById('result-sign-img');
        const resultSignName = document.getElementById('result-sign-name');
        const resultElement = document.getElementById('result-element');
        const resultDescription = document.getElementById('result-description');
        const resultAdvice = document.getElementById('result-advice');
        
        resultSignImg.src = `images/${zodiacData[resultSign].icon}`;
        resultSignImg.alt = resultSign;
        resultSignName.textContent = resultSign;
        resultElement.textContent = zodiacData[resultSign].element;
        resultElement.className = 'element-badge';
        
        // –î–æ–±–∞–≤–ª—è–µ–º –∫–ª–∞—Å—Å –¥–ª—è —Å—Ç–∏–ª–∏–∑–∞—Ü–∏–∏ –∑–Ω–∞—á–∫–∞ —ç–ª–µ–º–µ–Ω—Ç–∞
        switch(zodiacData[resultSign].element) {
            case '–û–≥–æ–Ω—å':
                resultElement.classList.add('fire-element');
                break;
            case '–í–æ–¥–∞':
                resultElement.classList.add('water-element');
                break;
            case '–í–æ–∑–¥—É—Ö':
                resultElement.classList.add('air-element');
                break;
            case '–ó–µ–º–ª—è':
                resultElement.classList.add('earth-element');
                break;
        }
        
        resultDescription.textContent = result.description;
        resultAdvice.textContent = result.advice;
    }
    
    function restartTest() {
        userAnswers = [];
        currentQuestion = 0;
        document.getElementById('test-result').classList.add('hidden');
        document.getElementById('test-start').classList.remove('hidden');
    }
}
